					<h4><b>Norma de Segurança 01</b></h4>
					<!--aqui tinha linha-->
					<p>Norma de segurança: em cumprimento à portaria 3.214, de 8 de junho de 1978, em sua nr-01 item 1.7, alínea a e b, item iii. A. Cumprir e fazer cumprir as disposições legais e regulamentares sobre segurança e medicina do trabalho b. elaborar normas sobre segurança e medicina do trabalho, dando ciência aos empregados com os seguintes objetivos: item III: dar conhecimento aos empregados de que serão passíveis de punição, pelo descumprimento das normas de segurança expedidas.</p>			
					<br>
					<ol>
						<li>As chaves dos portões de acesso à área de ensaios do BTSA ficarão sob a responsabilidade do vigilante;</li>
						<li>O vigilante terá um livro de registro no qual deverá ser relatado o motivo da necessidade de acesso à área restrita do BTSA, nome do solicitante, data e horário;</li>
						<li>Somente terão acesso as pessoas devidamente autorizadas pelo Diretor de Tiro ou por pessoas por ele nomeadas, através de documento que se encontra com o vigilante, para posterior verificação e arquivo;</li>
						<li>Somente o pessoal qualificado e habilitado poderá trabalhar no BTSA, e sempre acompanhado de outro especialista na área envolvida;</li>
						<li>Utilize sempre equipamentos de proteção individual quando estiver executando suas tarefas diárias;</li>
						<li>Antes de iniciar os trabalhos realize inspeção cuidadosa observando se não há vazamentos e ou rupturas de tubulações ou tanques;</li>
						<li>Verifique as condições das válvulas, indicadores, sensores, alarmes, quanto ao seu perfeito funcionamento. Mantenha atenção à pressão máxima de trabalho permitida pelos equipamentos;</li>
						<li>Observe rigorosamente as instruções do fabricante, quanto ao procedimento de operação e manutenção do BTSA;</li>
						<li>Não manuseie ou armazene produtos químicos e ou inflamáveis no interior do prédio de ensaio do BTSA;</li>
						<li>Mantenha as saídas e equipamentos de combate a incêndios permanentemente desobstruídos e conheça suas operações;</li>
						<li>Inspecione visualmente as partes elétricas quanto a interruptores de comando, possibilidades de curto circuitos e/ou partes vivas energizadas, comunique ao responsável as irregularidades ou correção;</li>
						<li>Use somente ferramentas adequadas para cada ação;</li>
						<li>Qualquer irregularidade que implique em risco de operação ou funcionamento do BTSA, suspenda a operação e comunique imediatamente à sua Supervisão.</li>
						<li>Em nenhuma hipótese deve ser excedida a capacidade máxima de trabalho dos equipamentos do BTSA;</li>
						<li>Durante os testes de segurança em válvulas de pressão, não se posicione diretamente de frente aos indicadores;</li>
						<li>O prédio do BTSA deve estar bem arejado e bem iluminado;</li>
						<li>Mantenha a ordem, arrumação e limpeza do seu local de trabalho;</li>
						<li>Registre todas as atividades no livro de ocorrências do BTSA.</li>
					</ol>	
