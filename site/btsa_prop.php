					<h3><b>Combustíveis:</b></h3>
					
					<h3><b>Hidrazina, Monometil Hidrazina e Dimetil Hidrazina Assimétrica</b></h3>
					<br><br>
					<ul>
						<li><b>Combustíveis: </b> As hidrazinas apresentam odores amoniacais. Aparência: líquido incolor, fumegante em contato com o ar. Em contato com o ar, a solução apresenta cor amarelada. São higroscópicas e absorvem dióxido de carbono. Substâncias inflamáveis porém estáveis, ou seja, resistentes a choques e atritos sem ignitar. Reagem violentamente com oxidantes fortes, como peróxidos, tetróxidos, ácido nítrico etc... Normalmente são envazados em tambores de aço inox e estocados em locais ventilados, isentos de calor e fagulhas.</li>
					</ul>
					<br>
					<hr width="100%" class="colorir" size="1px" ><h4><b>Riscos à Saúde</b></h4>
					<!--aqui tinha linha-->
					<ul>
						<li>Limite de Tolerância (8 h/dia – 40 h/semana): 0,5 ppm; A detecção por odores indica uma concentração de 1 a 3 ppm; Pode ser fatal se inalado, ingerido ou absorvido pela pele. Causa danos ao fígado, rins, sangue, pulmões e sistema nervoso; Pode causar reação alérgica na pele; Este produto é suspeito de ser cancerígeno; Pode provocar má formação fetal;</li>
					</ul>
					<br>
					<hr width="100%" class="colorir" size="1px" ><h4><b>Precauções, Manuseio e Armazenamento</b></h4>
					<!--aqui tinha linha-->
					<ul>
						<li>Manusear somente vestindo equipamento completo de proteção (luvas e botas de borracha butílica, macacão anti-ácido e máscara com filtro para hidrazinas)</li>
						<li>Os recipientes e equipamentos de manutenção devem estar eletricamente aterrados;</li>
						<li>Abrir o tambor cuidadosamente, para dar passagem aos vapores;</li>
						<li>Encher os tambores vazios com água até o transbordamento. Enxaguar completamente e drenar. Recolocar o tampão seguramente antes de devolver ao fornecedor. Desfazer-se do produto não utilizado e das águas de lavagem da maneira apropriada para este material.</li>
					</ul>
					<br>
					<hr width="100%" class="colorir" size="1px" ><h4><b>Derramamentos e Vazamentos</b></h4>
					<!--aqui tinha linha-->
					<ul>
						<li>Tratar somente vestindo equipamento completo de proteção pessoal; Reduzir os vapores pelo uso de névoa de água; Lavar com grande quantidade de água e drenar para uma bacia de coleta, não usar serragem, trapos ou outros materiais têxteis absorventes. Transferir para recipientes apropriados e vedar. Neutralizar qualquer material remanescente com solução diluída de hipoclorito (sódio ou cálcio) e lavar com água; Remover as roupas contaminadas imediatamente e lavar sem demora; Desfazer-se imediatamente de sapatos, cintos e outros materiais de couro, depois de deixá-los de molho em água.</li>
					</ul>
					<br>
					<hr width="100%" class="colorir" size="1px" ><h4><b>Primeiros Socorros</b></h4>
					<!--aqui tinha linha-->
					<ul>
						<li><b>Contato com os olhos:</b>  lavar imediatamente com água em abundância durante 15 minutos. Abrir as pálpebras e separá-las do globo ocular para assegurar que toda a superfície seja completamente lavada. Chamar um médico imediatamente;</li>
						<li><b>Contato com a</b> pele:  lavar imediatamente com água por 15 minutos, retirar as roupas e sapatos contaminados, chamar um médico imediatamente;</li>
						<li><b>Inalação:</b>  remover a vítima para ar fresco. Utilizar respiração artificial se a vítima não estiver respirando e oxigênio se a respiração estiver difícil. Manter a vítima aquecida e chamar um médico imediatamente;</li>
						<li><b>Ingestão:</b>  beber água e induzir o vômito. Chamar um médico imediatamente. Não dê nada pela boca se a vítima estiver inconsciente ou tendo convulsão.</li>
					</ul>
					<br>
					<hr width="100%" class="colorir" size="1px" ><h4><b>Oxidante</b></h4>
					<!--aqui tinha linha-->
					<ul>
						<li>O tetróxido de nitrogênio é um líquido marrom, volátil, produzindo vapores marrom avermelhados; mais pesados que o ar, os vapores descem após formação, podendo se acumular em espaços confinados;</li>
						<li>Odor irritante, perceptível a uma concentração de 0,2 ppm; Sóluvel em água, formando ácido nítrico e ácido nitroso; Não é inflamável porém alimenta a combustão (forte oxidante);</li>
						<li>Normalmente são envazados em cilindros de aço inox, na forma líquida, sob pressão e são estocados em locais ventilados.</li>
					</ul>
					<br>
					<hr width="100%" class="colorir" size="1px" ><h4><b>Riscos à Saúde</b></h4>
					<!--aqui tinha linha-->
					<ul>
						<li>Limite de Tolerância (8 h/dia – 40 h/semana): 4 ppm Ingestão pode causar queimaduras na boca, esôfago e estômago; Contato prolongado com a pele causará absorção de quantidades perigosas do gás; Inalação pode causar irritação das membranas, faringite, bronquite com dor de cabeça, cianose, respiração irregular, vertigem e a possibilidade de um edema pulmonar; Concentrações altas de vapor podem causar dor, bronco-constrição, reflexo vagaroso do coração e possibilidade de asfixia. A falta de oxigênio pode ser fatal.</li>
					</ul>
					<br>
					<hr width="100%" class="colorir" size="1px" ><h4><b>Precauções, Manuseio e Armazenamento</b></h4>
