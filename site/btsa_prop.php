						<li>Manusear somente vestindo equipamento completo de proteção (luvas e botas de borracha butílica, macacão anti-ácido e máscara com filtro para gases ácidos);</li>
						<li>Armazenar longe de materiais inflamáveis; O cilindro deve ser devolvido ao fabricante com a tampa da válvula de saída firmemente instalada</li>
