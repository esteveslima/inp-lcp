					<hr width="100%" class="colorir" size="1px" ><h4><b>Célula de ensaios do BTSA</b></h4>
					<!--aqui tinha linha-->
					<p>O BTSA foi projetado e construído por uma parceria entre o INPE e a empresa francesa SEP (Societé Européene de Propulsion), hoje SNECMA Moteurs.</p>

					<p>A altitude é simulada numa câmara de vácuo de 8,5m3 de volume ligado ao grupo principal de vácuo por meio de um canal equipado com uma válvula pneumática tipo guilhotina.</p>
					
					<p>Ejetores alimentados por vapor de água super-aquecido, e bombas de anel líquido são utilizados na criação do vácuo para a simulação da altitude. O vapor é gerado por um conjunto de caldeira e super-aquecedor. Os produtos de combustão mais vapor que se expandiu são descarregados num condensador tipo chuveiro, que utiliza 75m3/h de água à 11°C. Para o trocador de calor das bombas de anel líquido são necessários 30m3/h de água à 9°C. Portanto, para a produção de água fria o BTSA possui dois compressores de amônia (chillers), capazes de produzir 105m3/h de água fria à 8°C para alimentar o sistema.</p>
					
					<p>Após a condensação dos vapores no condensador mais a água utilizada no processo, o fluído vai para o separador de gases.</p>
					
					<p>Toda a água é armazenada para retornar ao sistema de refrigeração. Todos os subsistemas associados ao grupo principal de vácuo possuem armários elétricos que podem operar em modo local ou remoto.</p>
					
					<p>O ensaio de propulsores bipropelentes líquidos em câmaras de vácuo exige a elaboração de um checklist para a preparação do Banco de Testes para os ensaios. Inicialmente, o cliente, proprietário do motor, elabora um plano de testes para o propulsor. Em geral, estes testes envolvem operação no modo pulsado ou contínuo e em várias condições de funcionamento. Então, é feita uma reunião entre o Diretor de Tiro e a equipe (do Banco de Testes) com o cliente, na qual são definidos todos os parâmetros que deverão ser controlados e adquiridos durante os ensaios. Além da aquisição das medidas de temperatura, empuxo, pressão de câmara de combustão, vazão dos propelentes, nível de vácuo da câmara durante o teste, etc. Parâmetros de vigilância devem ser definidos para maior segurança. A partir daí, dois procedimentos simultâneos são iniciados pela equipe do BTSA, a Cronologia Geral do Banco (CGB) e a Cronologia Geral do Motor (CGM) que envolvem a descrição das operações ligadas ao BTSA e ao motor necessárias antes e depois de um teste de um motor bilíquido. Estas cronologias utilizam outros procedimentos de operação que serão distribuídos pelo Diretor de Tiro para os membros da equipe de especialistas do BTSA, como seguem:</p>

					<ul>
						<li>Procedimentos para preparação do grupo de vácuo;</li>
						<li>Procedimentos para sistema de propelentes;</li>
						<li>Procedimentos para sistema de medidas e tratamento de dados;</li>
						<li>Procedimentos para sistema de segurança;</li>
						<li>Procedimentos para preparação e instalação da balança de empuxo.	</li>
					</ul>
					<br>
					<hr width="100%" class="colorir" size="1px" ><h4><b>Água Gelada</b></h4>
					<!--aqui tinha linha-->
					<p>O sistema de produção de água gelada é formado por:</p>
					
					<ul>
						<li>esfriadores de líquido (Chillers, usando Amônia Anidra como fluido refrigerante);</li>
						<li>Torres de resfriamento ;</li>
						<li>Placas trocadoras de calor;</li>
						<li>Bombas de água.</li>
					</ul>
					
					<p>Esse conjunto tem a função de fornecer água para o condensador, as bombas de anel líquido e para lavagem dos gases produzidos pelo propulsor durante o ensaio na câmara de vácuo. Em pleno funcionamento o sistema fornece 75m3/h de água à 11ºC para o condensador, 30m3/h de água à 9ºC para as bombas de anel líquido, totalizando 105m3/h de água à 8ºC. No final do processo toda água é armazenada e retorna ao sistema de refrigeração.</p>
					
