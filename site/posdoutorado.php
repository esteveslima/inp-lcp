					<!--aqui tinha linha-->
					<center><p>Modelo Matemático de Spraylet</p></center> 
					
					<center><p>Fernando Fachini Filho ( Orientador )</p></center>
 					
					<center><p>Pesquisador Titular</p></center> 
					
					<center><p>Professor Pós-Graduação em Engenharia Aeroespacial</p></center> 
					
					<p>Propõe-se o desenvolvimento de um modelo matemático para descrever um elemento de spray reativo (spraylet), da mesma maneira como foi desenvolvido o elemento de chama (flamelet). O problema da combustão de sprays, como muito outros problemas da combustão e também de muitas outras áreas da Ciência, apresenta fenômenos em diversas escalas espaciais e temporais. A simulação numérica direta dos problemas da combustão de spray não é possível com a atual capacidade computacional. A solução para este impasse é incorporar modelos analíticos e/ou semi-analíticos que descrevem os processos das escalas menores nos modelos que descrevem os processos das escalas maiores. A aplicação da teoria de flamelet permitiu desacoplar o problema reativo do problema fluidodinâmico, o que reduziu consideravelmente o tempo de simulação da combustão turbulenta no regime não pré-misturado. Neste sentido, pretende-se seguir o mesmo procedimento que originou a teoria do flamelet e criar um modelo para descrever um elemento de chama considerando-se a dispersão de gotas no escoamento gasoso - elemento de spray reativo (spraylet). A teoria do elemento de chama (flamelet) considera a chama como uma seqüência de pequenos elementos e tem como hipótese que as menores escalas do escoamento (vórtices de Kolmogorov) são muito maiores que a espessura do elemento de chama. Esta hipótese garante que o elemento de chama se estabeleça em escalas nas quais o escoamento já pode ser considerado laminar. A turbulência apenas transporta os elementos de chama (flamelet) mas não altera sua estrutura. A idéia do elemento de spray reativo é reproduzir a teoria do flamelet admitindo parte do combustível ainda na forma líquida. Para garantir o sucesso do modelo, deve-se ainda admitir que as dimensões da gotas são muito menores que as da chama e do vórtice de Kolmogorov. A aplicação de teoria de spraylet possibilitará o desacoplamento da reação química do complexo escoamento turbulento bifásico.</p>
	
