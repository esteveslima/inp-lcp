					<h4><b>Breve Histórico do Desenvolvimento de Sistemas Micropropulsivos</b></h4>
					
					<p>Durante muitos anos, os cientistas ligados às pesquisas espaciais procuraram encontrar um propelente líquido capaz de decompor-se espontaneamente a um simples abrir de uma válvula, gerando gases com suficiente energia para propelir e estabilizar veículos espaciais.</p>
					
					<p>O Jet Propulsion Laboratory (JPL), nos Estados Unidos, começou a pesquisar o emprego da hidrazina anidra como propelente líquido, no início da década de 40. Os resultados mostraram que a decomposição térmica da hidrazina é lenta, com um tempo de resistência necessário de 35ms, que corresponde a um comprimento característico (L*) de aproximadamente 20m, o que é demasiadamente elevado para muitas aplicações práticas em propulsão.</p>
					
					<p>Mais tarde, o JPL desenvolveu um catalisador à base de Fe-Ni-Co/Al 2 O 3, eficiente para iniciar e manter a decomposição do monopropelente hidrazina, mas com um inconveniente: a necessidade de pré-aquecer o leito catalítico a temperaturas entre 593 e 693K. Mesmo assim, o desenvolvimento desse catalisador tornou viável o uso da hidrazina como monopropelente em sistemas propulsivos, se bem que a necessidade de um aquecimento prévio do leito catalítico tenha restringido significativamente o seu uso em veículos espaciais.</p>
					
					<p>O monopropelente hidrazina com o catalisador Fe-Ni-Co/Al 2 O 3 foi empregado pela primeira vez no veículo espacial lunar Able 4, desenvolvido em 1959 pelos Space Technology Laboratories (STL) e, posteriormente, nas missões Ranger e Mariner, desenvolvidas pelo JPL. Em todas essas missões, os sistemas propulsivos tiveram a finalidade de corrigir uma única vez a trajetória dos veículos espaciais.</p>
					
					<p>Durante anos, o JPL tentou desenvolver um catalisador capaz de decompor espontaneamente o monopropelente hidrazina, ou seja, decompô-lo a baixa temperatura (partida a frio - 293K); no entanto, foi a Companhia Shell que, no início da década de 60, conseguiu preparar tal material (Ir/Al 2 O 3, com elevado teor metálico - entre 30 e 35% em massa), denominando-o Shell 405 (atualmente designado como catalisador S-405 e fornecido pela Companhia Aerojet - USA).</p>
					
					<p>O desenvolvimento desse catalisador pode ser considerado um verdadeiro marco tecnológico, pois além de decompor espontaneamente a hidrazina, permite várias partidas a frio (múltiplas partidas), sendo esta a principal razão da opção de seu emprego em sistemas propulsivos.</p>

					<p>Mais tarde, já na década de 70, pesquisadores franceses desenvolveram o catalisador CNESRO, igualmente constituído de irídio suportado sobre alumina e apresentando a capacidade de decompor espontaneamente a hidrazina em partidas a frio.</p>
					
					<p> Tanto o catalisador Shell 405 quanto o CNESRO são constituídos unicamente de dois componentes:</p>
					
					<ul>
						<li>metal ativo: irídio metálico (Ir); e</li>
						<li>suporte inerte: alumina porosa (Al 2 O 3 ) na forma de grãos esferoidizados.</li>
					</ul>
					
					<p>A necessidade de um elevado teor de irídio e de uma distribuição uniforme deste metal sobre o suporte servem a três propósitos:</p>
					
					<ul>
						<li>ajudar na transferência do calor reacional em todo o grão, o que reduz consideravelmente o choque térmico do suporte, uma vez que a reação de decomposição da hidrazina é exotérmica e a temperatura reacional pode atingir até 1273K;</li>
						<li>aumentar a resistência mecânica do suporte;</li>
						<li>permitir que o catalisador continue ativo, mesmo após a perda de metal provocada pela erosão do grão.</li>
					</ul>
					
					<p>A alumina utilizada como suporte deve atender a uma série de especificações. Dentre as quais, pode-se citar:</p>
					
					<ul>
						<li>grande resistência mecânica;</li>
						<li>área específica elevada (>150,0m2/g);</li>
						<li>estabilidade térmica;</li>
						<li>resistência a choques térmicos;</li>
						<li>distribuição bimodal do volume de poros (microporos e macroporos).</li>	
					</ul>
