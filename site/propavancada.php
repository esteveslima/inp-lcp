					<p>É possível subdividir o estudo da propulsão espacial em função da fonte de energia utilizada para acelerar o material ejetado. A propulsão chamada convencional utiliza-se da energia interna armazenada nas ligações químicas dos propelentes utilizados. Portanto, neste caso, o propelente é ao mesmo tempo o fluido de trabalho e a fonte de energia. A propulsão avançada é um ramo de estudo da propulsão espacial que emprega fontes alternativas de energia para acelerar os propelentes, tais como a energia nuclear, a energia solar e a energia elétrica. Neste campo também se estuda a utilização de uma combinação destas fontes de energia. São exemplos de propulsores avançados: Foguete de Magnetoplasma com Impulso Específico Variável (VASIMIR), Foguete de Radio Isótopo, Propulsor Iônico com Grades, Propulsor de Efeito Hall, Propulsor Eletrotérmico, Propulsor de Plasma Pulsado, Propulsor Magnetoplasmadinâmico, Propulsor Laser e Vela Solar.</p>
