					<!--aqui tinha linha-->

					<p>O curso é constituído basicamente de três períodos letivos com duração aproximada de três meses. Por período, são oferecidas três ou mais disciplinas, cada qual valendo, em geral, três créditos. Normalmente, ao fim do primeiro ano, o aluno terá obtido um total mínimo de 24 créditos em cursos. Exige-se uma média geral mínima B para aprovação.</p>

					<p>As <a target="_blank" onclick="verificarlink(this)" href="docs/DisciplinasdoPCP2010.pdf" class="colorir">disciplinas oferecidas</a> compreendem aspectos fundamentais da engenharia aerospacial, mecânica e química, como dinâmica dos fluidos, combustão, aerotermoquímica, propulsão, teoria cinética dos gases, mecânica clássica, matemática aplicada, catálise e cinética química.</p>		
