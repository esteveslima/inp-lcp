					<h2  align="center" class="colorir">Catálise Industrial</h2>
					<hr width="100%" class="colorir" size="3px" ><br>				
				
					<p>O laboratório de Catálise do LCP/INPE desenvolveu um método próprio de conformação de óxidos em pó, em particular o óxido de nióbio, transformando-os em grãos extrudados ou esferoidizados, com significativos ganhos em propriedades importantes tais como área específica, volume total de poros, controle dos diâmetros de poros e resistência mecânica.</p>
					
					<p>Dessa forma, o óxido de nióbio produzido pela CBMM e moldado com a tecnologia desenvolvida no LCP, passou a ser utilizado em processos catalíticos efetuados em reatores de leito fixo industriais.</p>
					
					<p>Mais recentemente, com o apoio do CENPES/PETROBRAS, o LCP/INPE, mediante projeto em curso, iniciou um projeto de geração de H2 via recirculação química (Chemical Looping Reforming-CLR), visando geração de energia.</p>
					
					<p>Com os recursos deste projeto foi possível construir um novo edifício (ver figura 2), duplicando assim a área dos laboratórios do Grupo de Catálise.</p>
					<br>
					<center><img alt="Image" src="imagens/catalise_ind_pagimg.jpg"></center>
					
					<p>Catalisadores para geração de energia que empregam a tecnologia CLR estão sendo preparados, caracterizados e aplicados em diferentes dispositivos reacionais, tais como reator de leito fixo, reator de leito fluidizado e dois reatores acoplados, estes últimos, mediante recirculação, possibilitando a oxidação e a redução do catalisador sólido, denominado genericamente de transportador de oxigênio. Desta forma, H2 pode ser gerado, pela combustão de reagentes, tais como CH4, gás natural, ou carvão gaseificado, sem que haja participação do ar atmosférico.</p>
					
					<p>Dentre os catalisadores já investigados, o material mais eficiente obtido, até o momento, tem sido NiO/α-Al2O3, com teores em massa de NiO inferiores a 10%.</p>
					
					<p>O processo CLR, além de possibilitar a geração de energia, pela produção de H2 a partir de combustíveis fósseis, permite a captura ou eliminação de CO2 formado durante suas primeiras fases, diminuindo desta forma qualquer impacto sobre o meio ambiente.</p>

					<p>Estão ainda sendo estudados, neste Laboratório, novos catalisadores à base de nanofibras de carbono, na forma macroscópica, para emprego na reação de hidrogenação do CO (síntese de Fisher-Tropsch) com vistas à obtenção de hidrocarbonetos. Esses catalisadores têm apresentado uma alta seletividade em hidrocarbonetos entre C8 e C23, sendo estas pesquisas apoiadas financeiramente pela FAPESP.</p>					
