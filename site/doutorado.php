					<h2  align="center" class="colorir">Doutorado</h2>
					<hr width="100%" class="colorir" size="3px" ><br>				

					<h3><b>Requisitos Para Titulação</b></h3>
					<br>
					<hr width="100%" class="colorir" size="1px" ><h4><b>Entrevista de Qualificação</b></h4>
					<!--aqui tinha linha-->
					<p><u>Data limite:</u> até o final do primeiro período letivo cursado pelo aluno</p>

					<p>A Entrevista de Qualificação, respeitando o que estabelece o Art. 28 do Regimento dos Cursos de Pós-Graduação do INPE, determinará o plano geral de estudos e trabalhos do aluno, incluindo as disciplinas, e recomendará ao Conselho do Curso a aceitação de créditos já obtidos pelo candidato.</p>
					
					<p>A Comissão “Ad hoc” será indicada pelo Conselho de Curso, presidida pelo seu Coordenador Acadêmico do Curso, e composto por, no mínimo, mais três pesquisadores competentes nas áreas do Curso em questão.</p>
					
					<p>Após a análise do currículo acadêmico do aluno, a Comissão “ad-hoc” pode decidir recomendar ao Conselho de Pós-Graduação (CPG):</p>
					
					<p>Confirmação da data de admissão do aluno;</p>
					
					<ul>
						<li>Aceitação do mestrado concluído pelo aluno, ao qual correspondem 24 créditos em disciplinas no cômputo para o doutorado;</li>
						<li>Aceitação do exame de Inglês, prestado durante o Curso de Mestrado;</li>
						<li>Definição das disciplinas que o aluno deverá cursar, num total de, no mínimo, 12 créditos; e</li>
						<li>Cursar outras disciplinas a serem combinadas entre o aluno e seu orientador, num total de, no mínimo, 08 créditos;</li>
					</ul>
					
					<p>Obs: A soma total de créditos das disciplinas dos itens 4 e 5 será de, no mínimo, 24 créditos.</p>
					<br>
					<hr width="100%" class="colorir" size="1px" ><h4><b>Exame de Qualificação</b></h4>
					<!--aqui tinha linha-->
					<p>Data limite: até o término do período letivo consecutivo ao último período de obtenção de créditos necessários em disciplinas, não podendo o prazo para a sua realização exceder a <b>dois terços</b> do tempo disponível para a conclusão do Curso.</p>

					<p>A não realização do Exame no prazo estabelecido implicará a <u><b>reprovação do aluno</b></u>.</p>
					
					<p>O aluno reprovado no <b>exame de qualificação</b> terá uma única oportunidade adicional para prestá-lo até 60 dias após o primeiro exame, mantendo-se os critérios deste; em caso de nova reprovação, o aluno será <u><b>desligado</b></u> do Curso.</p>
					
					<p>O Exame de Qualificação, atendendo o que dispõe o Art. 30 do Regimento dos Cursos de Pós-Graduação de INPE, deverá ser efetuado como se segue:</p>
					
					<ul>
						<li>A Banca Examinadora, indicada pelo Conselho de Curso, organizará o Exame de Qualificação no mínimo 40 dias antes do término do prazo concedido para sua realização.</li>
						<li>O Exame será <b>escrito e oral</b> versando sobre temas consistentes com a Entrevista de Qualificação da área de Concentração em Combustão e Propulsão.</li>
						<li><b>O exame escrito</b> será constituído de uma questão para cada uma das disciplinas básicas em Combustão e Propulsão: combustão, propulsão, mecânica dos fluidos, escoamento compressível e matemática, com duração total máxima de 4 horas.</li>
						<li><b>O exame oral</b> será constituído de cinco perguntas para cada uma das disciplinas básicas em Combustão e Propulsão: combustão, propulsão, mecânica dos fluidos, escoamento compressível e matemática, com duração máxima de 20 minutos para cada disciplina.</li>
					</ul>
					<br>
					<hr width="100%" class="colorir" size="1px" ><h4><b>Exame de proposta de tese</b></h4>
					<!--aqui tinha linha-->
					<p>Data limite: até o término do <b>terceiro período</b> letivo consecutivo após o período de obtenção de créditos necessários em disciplinas, não podendo o prazo para a sua realização <b><u>exceder a dois terços</u></b> do tempo disponível para a conclusão do Curso;</p>

					<p>A não realização do Exame no prazo estabelecido implicará a reprovação do aluno.</p>
					
					<p>A Proposta deverá ser acompanhada de declaração do Coordenador Acadêmico do Curso ETE e do Chefe do LCP envolvidos, atestando o interesse do INPE e a existência de meios para viabilizá-la;</p>
					
					<p>A Banca Examinadora, indicada pelo Conselho de Curso, deverá ser constituída, no mínimo, 30 dias antes do <b>término do prazo</b> concedido ao aluno para a realização do <b>exame de proposta de tese</b>.</p>
					
					<p>A Banca Examinadora verificará a validade, a originalidade e a viabilidade da Proposta.</p>
					
					<p>O Exame de Proposta de Tese constará da exposição oral de uma <b>proposta escrita</b> sobre o assunto da tese, com aproximadamente 20 páginas datilografas, contendo:</p>
					
					<ul>
						<li>O objetivo</li>
						<li>A revisão bibliográfica</li>
						<li>A metodologia</li>
						<li>Resultados esperados</li>
						<li>Referências bibliográficas</li>
						<li>Cronograma de execução das atividades</li>
						<li>Clique  <a target="_blank" onclick="verificarlink(this)" href="docs/DadosparamarcaroExamedePropostadeTese-2006.doc" class="colorir">aqui</a>  para saber os dados necessários para marcar o Exame</li>
						<li>As cópias da Proposta deverão ser entregues ao Coordenador do Curso PCP/ETE, no mínimo, <b><u>quinze (15) dias</u></b> antes da data de apresentação</li>
					</ul>
					<br>
					<hr width="100%" class="colorir" size="1px" ><h4><b>Defesa Final de Tese</b></h4>
					<!--aqui tinha linha-->
					<p>A defesa da tese deverá ser realizada em conformidade com Art. 37 (pág. 13) do Regimento dos Cursos de Pós-graduação do INPE.</p>

					<p>A defesa da tese somente poderá ser realizada após o aceite para publicação de um artigo, na versão final, em uma <u>revista indexada</u> na Área de Concentração em Combustão e Propulsão.</p>
					
					<p>O aluno deverá estar quite com o SPG/INPE - São José dos Campos;</p>
					
					<p>O orientador deverá indicar, ao Conselho de Área de Concentração, os nomes dos membros da banca examinadora e dos suplentes.</p>
					
					<p>Após a definição dos membros da banca examinadora pelo Conselho de Área, o aluno deverá entregar ao Coordenador do ETE/PCP, no mínimo 30 dias antes da data marcada para a defesa, as cópias da tese bem como os dados necessários para marcar a defesa (<a target="_blank" onclick="verificarlink(this)" href="docs/Modelo_ Dados_Defesa_TESE.doc" class="colorir">clique aqui</a>):</p>
					
					<p>O aluno deverá enviar para a editoração (biblioteca digital), 30 dias antes da data marcada para a defesa, uma cópia da tese, em PDF, para que seja revisada.</p>
	
					<p>A Banca Examinadora será composta de <b>cinco</b> a <b>sete</b> membros com Título de Doutor (<b>membros externos</b>, ao INPE, no mínimo <b>dois</b> e no máximo <b>três</b>):</p>
					
					<p>Composição da Banca Examinadora (mínima):</p>
					
					<ul>
						<li>Orientador - Presidente</li>
						<li>Dois Docentes do ETE/PCP</li>
						<li>Dois Membros Externos ao INPE</li>
						<li><b>Três membros suplentes</b> para a Banca Examinadora da defesa de tese deverão ser designados, sendo pelo menos dois deles externo ao Corpo Docente do Curso do ETE</li>
					</ul>
					
