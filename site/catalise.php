					<h2  align="center" class="colorir">Catálise</h2>
					<hr width="100%" class="colorir" size="3px"><br>
					
					<h4><b>Introdução</b></h4>
					
					<p>A catálise consiste no emprego de materiais que participam de reações químicas, sem serem consumidos, mas por modificarem o mecanismo dessas reações, permitem que elas sejam efetuadas a menores temperaturas e/ou com uma velocidade reacional muito maior.</p>
					
					<p>Classicamente, a catálise é subdividida em três grandes áreas:</p>
					<br>
					<ul>
						<li><h4><b>Catálise Heterogênea</b></h4>
						onde catalisador, reagentes e produtos reacionais encontram-se em mais de uma fase física. Também denominada catálise de contato, é a mais empregada em processos industriais, tais como aqueles ligados à petroquímica;</li>
						<li><h4><b>Catálise Homogênea</b></h4>
						onde catalisador, reagentes e produtos reacionais encontram-se em uma única fase física. É muito utilizada na produção de fármacos e produtos medicinais.</li>
						<li><h4><b>Catálise Enzimática</b></h4>
						onde o catalisador é uma enzima, a qual favorece a produção de compostos biológicos, importantes na área agrícola.</li>
					</ul>
					<br>
					<p>Atualmente, mais de 70% dos processos presentes na indústria química envolvem pelos menos uma etapa catalítica.</p>
					
					<p>No LCP/INPE, as atividades em catálise, iniciadas em 1984, estão sendo realizadas na área heterogênea, com o emprego de catalisadores sólidos em reações com gases ou sólidos, focadas no desenvolvimento de catalisadores com vistas à propulsão de satélites, plataformas orbitais e no rolamento de veículos lançadores de satélites e a processos de combustão com captura do CO2. Além disso, o grupo de catálise do LCP atua na formação de recursos humanos na área e na prestação de serviços à indústria química e às universidades do país.</p>
					
					<p>Em resumo, as atividades em catálise no LCP/ INPE envolvem química, técnicas de preparação e caracterização de materiais, engenharia ligada a processos industriais e ambientais e engenharia ligada à propulsão.</p>
					<br>
					<hr width="100%" class="colorir" size="1px" ><h4><b>Catálise aplicada à Propulsão</b></h4>
					<!--aqui tinha linha-->
					<p>A propulsão de satélites destina-se a manter os mesmos em órbita correta durante toda sua vida útil, corrigindo sua atitude (antenas voltadas para a terra e coletores de energia solar voltados para o espaço).</p>
				
					<p>No início da década de 60, o JET PROPULSION LABORATORY (JPL), nos Estados Unidos, conseguiu obter um catalisador, constituído de irídio suportado em uma alumina (Ir/Al2O3), com elevado teor metálico em massa (30% a 35%), denominado à época SHELL 405 (atualmente S-405 produzido pela companhia AEROJET-USA). Este catalisador foi considerado um verdadeiro marco tecnológico, pois além de decompor espontaneamente o monopropelente hidrazina (N2H4) em produtos gasosos (N2, H2 e NH3), a altas temperaturas e pressões, permitia também vários acionamentos a frio, fato importante nos acionamentos urgentes sem aquecimento do propulsor.</p>

					<p>Outros países desenvolveram catalisadores semelhantes, entre eles, Rússia e França. No caso do Brasil, a aquisição do catalisador americano sempre teve restrições e condicionantes impostas pelo governo dos USA, o que obrigou o LCP/INPE a desenvolver um catalisador Ir/Al2O3 nacional.</p>
					
					<p>Para tanto, o LCP/INPE, a partir de 1984, começou a montar uma equipe de profissionais em catálise e contou, por algum tempo, com o auxílio do CENPES/PETROBRAS e da Université Pierre e Marie Curie (Paris-França). A maior dificuldade consistiu em preparar uma alumina especial, com características morfológicas e físico-químicas adequadas, e formatar este material com dimensões adequadas ao emprego em propulsão.</p>
					
					<p>Hoje, o catalisador nacional Ir/Al2O3 (LCP-33R) é uma realidade, já tendo sido testado em múltiplas condições e tornado o Brasil independente da importação deste material estratégico.</p>
					
					<p>A catálise heterogênea também pode ser aplicada a motores de rolamento de veículos lançadores de satélite. Diferentemente da aplicação na propulsão de satélites, aqui as quantidades de catalisador utilizadas são muito maiores, mas durante um único acionamento e durante poucos minutos.</p>
					
					<p>Sendo o irídio um metal extremamente raro e caro, o Grupo de Catálise do LCP/INPE desenvolveu catalisadores menos dispendiosos, a saber:</p>
					<br>
					<ul>
						<li>catalisadores de rutênio sobre alumina (Ru/Al2O3) e de irídio e rutênio sobre alumina (Ir-Ru/Al2O3), aproveitando o fato de o rutênio ser cerca de 20 vezes menos caro do que o irídio;</li>
						<li style="margin-top: 10px">catalisadores à base de nitretos e carbetos de tungstênio e molibdênio. Neste caso, todas as tecnologias de preparação, formatação, caracterização e aplicação à propulsão foram desenvolvidas no LCP/INPE.</li>
					</ul>
					<br>
					<p>Dentre os materiais acima citados, aqueles contendo o rutênio e os constituídos de carbeto de tungstênio foram testados visando aplicação em motores de rolamento de veículos lançadores de satélites, com excelentes resultados.</p>
					
					<p> Atualmente, uma nova geração de catalisadores aplicados à propulsão está sendo investigada. Os materiais são constituídos por irídio depositado sobre alumina e nióbia (Ir/Al2O3-Nb2O5). Estes catalisadores estão sendo preparados no LCP/INPE e os primeiros resultados obtidos em propulsão mostraram-se promissores.</p>
					
					<p>O LCP vem ainda desenvolvendo catalisadores mássicos à base de óxidos para a decomposição do peróxido de hidrogênio concentrado para emprego em sistemas micropropulsivos de satélites e como oxidante em foguetes hídricos.</p>
					
