					<h4><b>Política da Qualidade</b></h4>
					<!--aqui tinha linha-->
					<p>O Banco de Testes com Simulação de Altitude (BTSA) possui um sistema de gestão que objetiva o reconhecimento nacional e internacional para a qualificação de sistemas propulsivos para aplicações espaciais.</p>

					<p>A Política da Qualidade do BTSA é definida pela satisfação dos clientes internos e externos, priorizando o fornecimento de serviços que agregam valor ao Cliente e ao BTSA de forma compartilhada, trabalhando com eficácia, ética e segurança, aplicadas ao cumprimento de objetivos.</p>
					
					<p>O grau de excelência atingido pelo BTSA se deve ao alto nível dos especialistas envolvidos que, continuamente, se aprimoram em suas atividades e responsabilidades. Nas relações com seus colaboradores, o BTSA se empenha em manter-se capacitado, ofertando serviços integrados que atendam aos quesitos dos clientes. As ações especificadas destinam-se a garantir a qualidade em todas as etapas do processo.</p>
					
					<p>Com relação ao desenvolvimento tecnológico, o BTSA encontra-se em permanente evolução, buscando sempre ser reconhecido como um centro de referência internacional no desenvolvimento de tecnologia espacial.</p>
