					<p>No campo da dinâmica de gases rarefeitos é de fundamental importância estudar, entre outros, os fenômenos relacionados com escoamentos ao redor de aeronaves em grandes altitudes, reentrada de veículos espaciais, escoamentos hipersônicos sobre bordos de ataque pontiagudo e rombudo, escoamentos hipersônicos com reações químicas, desempenho e plumas de micropropulsores, estrutura de ondas de choque em bordos de ataque, interação choque-choque, características refletivas e reativas de gases interagindo com superfícies sólidas, etc. O estudo desses fenômenos, onde geralmente a estrutura molecular do gás não pode ser ignorada, representa o tema central no campo da dinâmica de gases rarefeitos.</p>					
					
					<p>A dificuldade de dispositivos experimentais no estudo desses fenômenos, onde vários processos físicos e químicos altamente complexos são relevantes, tem estimulado o desenvolvimento de vários métodos numéricos de modo a simular tais fenômenos. Em face ao grau de rarefação, esses fenômenos são simulados numericamente pelo método Direct Simulation Monte Carlo (DSMC). O método DSMC tem sido reconhecido como o único meio prático de simular escoamentos rarefeitos de interesse em engenharia. Neste cenário, o Grupo de Simulação Numérica de Escoamento Rarefeito busca soluções de problemas de grande relevância prática que possam ser utilizadas no desenvolvimento de novas tecnologias espaciais de um modo mais rápido e a uma fração dos custos de outras possíveis abordagens desses problemas.</p>
				
