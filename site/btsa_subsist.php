					<hr width="100%" class="colorir" size="1px" ><h3><b>Subsistema de Propelentes</b></h3>
					<!--aqui tinha linha-->
					<p>O subsistema de propelentes é constituído dos reservatórios e das linhas de alimentação dos propelentes (combustíveis e oxidante).</p>

					<p>Combustíveis</p>
					
					<p>Os combustíveis utilizados são hidrazina, monometil-hidrazina e dimetil-hidrazina assimétrica. Os combustíveis são envazados em tambores de aço inox e estocados em locais ventilados, isentos de calor e fagulhas. O manuseio é feito somente com EPI (luvas e botas de borracha butílica, macacão anti-ácido e máscara com filtro para hidrazinas).</p>
					
					<p>Oxidante</p>
					
					<p>O oxidante utilizado é o TETRÓXIDO DE NITROGÊNIO. O tetróxido de nitrogênio é um líquido marrom e volátil que produz vapores marrom-avermelhados mais pesados que o ar. Os vapores descem após formação, podendo se acumular em espaços confinados. Normalmente são envazados em cilindros de aço inox, na forma líquida, sob pressão e são estocados em locais ventilados.</p>
					<br><br>
					<hr width="100%" class="colorir" size="1px" ><h3><b>Subsistema de Aquisição e Tratamento de Dados</b></h3>
					<!--aqui tinha linha-->
					<p>A aquisição e o tratamento dos dados são feitos na sala de controle do BTSA. Esta sala contém toda a infraestrutura (hardware e software) para aquisição de dados, comando e controle de todos os subsistemas que compõem o BTSA. Os ensaios são todos gravados em arquivos digitais de imagens e infravermelho.</p>
