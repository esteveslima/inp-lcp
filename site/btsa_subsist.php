					<h4><b>Grupo de Vácuo Principal</b></h4>
					
					<p>O grupo de vácuo principal é constituído por:</p>

					<p>Câmara de Vácuo Principal</p>
					
					<p>A altitude é simulada numa câmara de vácuo de 10 m3 de volume útil, ligada a um grupo de vácuo por um canal equipado com uma válvula pneumática tipo guilhotina.</p>
					
					<p>Bombas e Ejetores</p>
					
					<p>Ejetores alimentados por vapor d'água super aquecido e bombas de anel líquido são utilizados na criação do vácuo para a simulação da altitude. </p>
					
					<p>Gerador de Vapor<br>
					O vapor para os ejetores é gerado por uma caldeira e um super aquecedor.</p>
					
					<p>Refrigeração<br>
					O BTSA possui dois compressores de amônia (chillers) capazes de produzir 110 m3 por hora de água fria a 8 graus C. A água gelada é utilizada para diluir gases poluentes gerados nos ensaios e no funcionamento das bombas de anel líquido. Após a condensação, os vapores mais a água utilizada no processo vão para um separador. Toda a água é armazenada para retornar ao sistema de refrigeração.</p>
					
					<p>Controle de Energia</p>
					
					<p>Os subsistemas associados ao grupo principal de vácuo possuem armários elétricos que podem operar em modo local ou remoto. A demanda de energia elétrica para operação do BTSA é da ordem de 900 kW.</p>
					
