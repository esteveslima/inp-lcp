					<p>Todo aluno de doutorado que receber ao menos uma bolsa CAPES (<b>proex ou demanda social</b>) terá obrigatoriamente que realizar o <b>estágio de docência</b>.</p>

					<p>Os alunos do ETE/PCP são regidos pela Portaria N° 76, de 14 de abril de 2010 - <b>demanda social</b>. O artigo 18 contém dez (10) itens: o item V do artigo 18 determina que o estágio de docência deve ser concluído em até três semestres, com uma carga total de <u>120 horas.</u></p>
					
					<p><u>Outras considerações:</u></p>
					
					<ol>
						<li>O aluno que comprovar ter lecionado em colégios, através de Carteira Profissional ou de Declaração de docente, emitida pela Escola, será aceito como estágio de docência</li>
						<li>O aluno tem a liberdade de escolha onde realizar o estágio em curso de graduação (público ou privado) e deverá, após a sua conclusão, apresentar um relatório das atividades desenvolvidas, demonstrando o total da carga horária</li>
						<li>Apresentar também o Termo de Compromisso de Estágio de Docência com as assinaturas identificadas dos envolvidos</li>
						<li>Por fim, o Coordenador Acadêmico deverá endereçar ao SPG um memorando, com os documentos citados, relatando que o  aluno(a) cumpriu a exigência do estágio com o total da carga horária exigida pela CAPES</li>
