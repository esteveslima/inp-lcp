					<p>Resumo:</p>
						
					<p>O projeto proposto destina-se a realizar uma análise teórica das  características de chamas difusivas, provenientes da queima de combustíveis  líquidos, estabelecidas dentro de um meio poroso inerte. O fenômeno de  combustão em meios porosos está presente em uma gama enorme de aplicações,  de queimadores industriais radiantes, passando pela recuperação terciária de  petróleo (combustão in-situ), até a queima de propelentes sólidos de busters  e decomposição de propelentes líquidos de foguetes de controle de atitude,  por isso tem grande interesse tecnológico e científico. O desenvolvimento do  projeto irá focar nas condições da combustão in-situ, porém seu caráter  fundamental permitirá a análise de qualquer dos casos mencionados. Devido  aos processos de transporte atuarem de modo particular em diferentes escalas  espaciais, o processo da combustão pode ser descrito analiticamente por meio  do método das perturbações singulares. O desenvolvimento do projeto levará a  uma nova modelagem matemática da estrutura de chamas difusivas provenientes  da queima de combustíveis líquidos em meios porosos. Este modelo (sub-malha)  poderá ser empregado na transferência das informações das escalas pequenas  (de ordem do décimo do metro) para a escala da simulação da combustão  in-situ, que depedendo do problema estudado pode variar da ordem do metro  até a ordem do quilômetro.</p>
