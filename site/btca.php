					<hr width="100%" class="colorir" size="1px" ><h4><b>Instalações</b></h4>
					<!--aqui tinha linha-->
					<p>O BTCA foi concebido pelo Dr. José Gobbo Ferreira e a sua construção foi iniciada em 1996 com apoio financeiro do CNPq, FINEP, INPE e FAPESP. As instalações incluem um ponto fixo, onde são montados os motores para testes, linhas de alimentação para combustíveis, oxidantes e monopropelentes, tanques de propelentes e uma sala de controle. Por razões de segurança, as linhas de propelentes são divididas em duas baias, uma para a parte gasosa e outra para o propelente pressurizado. Todas essas três seções são separadas entre si por paredes de concreto. As linhas são alimentadas por um conjunto de cilindros de nitrogênio aeroespacial. Os tanques dispõem de visor de nível e medição local de pressão. Circuitos de despressurização permitem que os vapores sejam neutralizados antes de liberá-los para a atmosfera, além de contar com atuadores pneumáticos controlados remotamente. As três linhas contam com Medidores de vazão do tipo Coriolis. Todos os componentes das linhas que podem entrar em contato com os propelentes são de aço inoxidável ou PTFE. O banco dispõe de uma bancada de aferição e calibração de sensores e transmissores, recentemente certificada em procedimento rastreado pelo INMETRO, com capacidade para receber transmissores de tensão, corrente e frequência, bem como sensores de temperatura, pressão e força nas mais variadas escalas. Conta ainda com os serviços da oficina mecânica com capacidade para usinagem de precisão e do Laboratório Químico para as análises de propelentes e efluentes. </p>

