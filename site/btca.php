					<hr width="100%" class="colorir" size="1px" ><h4><b>Instrumentação</b></h4>
					<!--aqui tinha linha-->
					<p>Propulsores químicos para satélites e foguetes são componentes de alta tecnologia que necessitam de condições especiais para o seu desenvolvimento e teste. As principais características dos sistemas de comando dos testes e de aquisição de dados implementados no BTCA, são:</p>
					
					<ul>
						<li>comando do motor totalmente automatizado</li>
						<li>possibilidade de comandar manualmente caso necessário</li>				
						<li>armazenagem das seqüências de testes individuais para cada tipo de propulsor</li>					
						<li>tempos de comando extremamente precisos</li>
						<li>dados adquiridos em cada teste armazenados individualmente</li>
						<li>mix de dados com alta taxa de aquisição e alta resolução</li>						
						<li>programação de limites de segurança (alarmes) para cada grandeza, monitorados em tempo real</li>						
						<li>ações de emergência programadas e executadas automaticamente</li>		
						<li>mínimo tempo entre a detecção do alarme e a execução da ação de emergência</li>						
						<li>registro visual dos testes (videos) com redundância</li>
					</ul>
					<br>
					<center><img alt="Image" src="imagens/btca_pagimg3.jpg">
					
					<p>Esquema do Sistema de Controle e Aquisição</p></center>
					<br>
					<p>O sistema de controle e aquisição foi concebido para maximizar a utilização das instalações existentes, sendo um sistema modular que permite a programação de testes para os mais diversos tipos de propulsores. Os dados, tais como pressões nas entradas de propelente, na câmara de combustão, vácuo na câmara de teste, empuxo, temperaturas e vibração são monitorados e registrados. Câmeras de vídeo acompanham o ensaio em vários ângulos Uma câmera de vídeo infravermelho também pode ser usada para visualização da distribuição de temperaturas. Um sistema de segurança desabilita qualquer eventual comando manual ou automático vindo da Sala de Comando, protegendo os operadores, as instalações e o propulsor em teste. O Comando manual permite pilotar as válvulas, bem como desabilitar todo o sistema. Além disso fornece um retorno visual do estado dos comandos, independente do sistema automático. O Sistema Real-Time contêm placas de aquisição e controle de alta velocidade e resolução, condicionamento de sinais, fontes de energia e drivers de saída. Utiliza um sistema operacional específico e se comunica com a Interface Homem-Máquina através de uma rede de alta velocidade.</p>

					<p>Além dos programas supervisórios e de controle dos testes, são desenvolvidos programas específicos para a análise dos dados, permitindo a visualização imediata, após cada teste, das curvas de todos os parâmetros, com análise matemática dos dados e valores estatísticos.</p>
