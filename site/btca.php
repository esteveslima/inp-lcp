					<ul>
						<li>Foguete a propelente sólido</li>
					</ul>
					
					<p>No campo da Propulsão Sólida, foi desenvolvido no BTCA um motor a propelente composite para emprego em um foguete para captura de descargas atmosféricas.</p>
					
					<p><!--iframe--></p>
					
					<p>O vídeo mostra, na ordem, os ensaios para seu desenvolvimento, o teste de sua configuração definitiva, o ensaio de qualificação do motor em vôo de foguete e, por último, a captura de um raio durante o dia e de outro à noite. O foguete é lançado e ao subir, desenrola atrás de si uma bobina de fio com alma metálica que, ao se desintegrar, cria um caminho para a descarga. São feitos principalmente dois tipos de testes: no primeiro, chamado de 'Tiro conectado', todo o fio da bobina contêm a alma metálica, o que permite conduzir a descarga até a própria torre de lançamento, para o levantamento de suas características. O segundo é chamado de 'Tiro não conectado', no qual junto à torre existem diversos para-raios e os últimos 100m do fio da bobina não contêm a alma metálica. Assim a descarga é 'solta' naquela altura e teoricamente escolhe o para-raio mais eficiente.</p>
