					<p><b>Resumo</b><br>
					O hidrogênio é reconhecidamente um dos prováveis combustíveis alternativos a ser empregado no futuro. Porém sua utilização não deve ocorre imediatamente por causa do tema segurança. Prevê-se que o uso seja em misturas com outros combustíveis comercialmente estabelecidos. A adição de hidrogênio na queima de hidrocarbonetos traz benefícios. Um deles é o aumento da estabilidade da chama. Chamas podem ser comparadas a elásticos, que uma vez estirados diminuem de espessura. A chama tem o mesmo comportamento quando o escoamento aplica-lhe um estiramento e este processo torna a chama mais propicia a extinção. O hidrogênio torna a chama do hidrocarboneto menos sensível ao estiramento. Um outro ponto a favor da mistura é a limpeza da chama. O hidrogênio reduz a formação de fuligem. O estudo visa quantificar numericamente o efeito da adição de hidrogênio na produção de fuligem</p>
				
				
				
