					<p><b>Resumo</b><br>
					Pela queima de combustível liquido, mais da metade da energia mundial é gerada. Este fato impõe um peso significativo na compreensão dos processos controladores da combustão de sprays. O objetivo desse projeto vai de encontro a nessa necessidade. O tema em estudo é a descrição da estrutura interna das chamas difusivas considerando a presença de gotas no interior da chama. Tanto a estrutura deste tipo de chama bem como os processos de vaporização e combustão de gotas estão muito bem descritos e isto ajudará no desenvolvimento de um modelo para chama com gotas. A base dessa análise é a teoria de flamelet, que consiste em tomar a chama como uma sucessão de pequenos elementos de chama e analisados independentemente para saber se estão acessos ou apagados. Baseado nesta teoria e nos conhecimentos de estrutura interna de chama e combustão de gotas, será desenvolvido uma teoria de spraylet: elementos de chama com gotas em seu interior. A aplicação dessa teoria deve ampliar as bases do conhecimento da combustão de sprays.</p>
				
