					<hr width="100%" class="colorir" size="3px" ><br>				
				
					<p>O projeto de captura de CO2 via combustão de hidrocarbonetos gasosos com recirculação química (Chemical Looping Combustion – CLC) está sendo executado, mediante o apoio financeiro do CENPES/PETROBRAS e do Centro de Sistemas Terrestres do INPE, com coordenação geral do Dr. Carlos Nobre.</p>
					
					<p>A combustão de hidrocarbonetos sólidos, líquidos e gasosos gera uma enorme quantidade de CO2, hoje reconhecido como a principal causa do aquecimento global. A liberação de CH4 e gás natural para a atmosfera, que ocorre nos poços de petróleo e na indústria petroquímica, é igualmente prejudicial ao meio ambiente, já que o CH4 é um gás com potencial para o efeito estufa cerca de 20 vezes maior do que o CO2.</p>
					
					<p>O processo CLC efetua a combustão em dois reatores acoplados, onde um sólido, denominado transportador de oxigênio, é continuamente reduzido e oxidado mediante recirculação. Os produtos formados, CO2 e H2O são obtidos em ausência de ar. Após separação da água por condensação, o CO2 é comprimido e estocado em sítios geológicos a grandes profundidades (> 800m), principalmente em poços desativados de petróleo e em aquíferos salinos.</p>
					
					<p>Apesar do processo CLC não ser catalítico, os seus reatores contém transportadores de oxigênio semelhantes aos do processo CLR.</p>

					<p>Dentre os materiais testados até o presente, aquele que vem apresentando os melhores resultados é NiO/α-Al2O3, com teores em massa de NiO superiores a 20%.</p>
					
					<p>Muitos processos industriais emitem certas quantidades de H2S na atmosfera. Em virtude das legislações mais severas, o LCP também tem se preocupado com a captura deste gás nocivo. Foi então desenvolvido um filtro à base de nanofibras de carbono altamente seletivo em enxofre elementar, trabalhando a baixas temperaturas (60ºC).</p>
